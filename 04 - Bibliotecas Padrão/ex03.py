import math

# Calcular a raiz quadrada de um número
numero = 16
raiz = math.sqrt(numero)
print(f"A raiz quadrada de {numero} é {raiz}")

# Calcular o valor de pi
pi = math.pi
print(f"O valor de pi é {pi}")

# Calcular a potência de um número (por exemplo, 2 elevado a 3)
potencia = math.pow(2, 3)
print(f"2 elevado a 3 é {potencia}")

# Calcular o valor do cosseno de um ângulo (em radianos)
angulo = math.radians(45)  # Converte 45 graus para radianos
cosseno = math.cos(angulo)
print(f"O cosseno de 45 graus é {cosseno}")
