import math

def calcular_raiz_quadrada(numero):
    return math.sqrt(numero)

# Testando a função com diferentes números
print(f"A raiz quadrada de 25 é: {calcular_raiz_quadrada(25)}")
print(f"A raiz quadrada de 49 é: {calcular_raiz_quadrada(49)}")
print(f"A raiz quadrada de 9 é: {calcular_raiz_quadrada(9)}")
print(f"A raiz quadrada de 2 é: {calcular_raiz_quadrada(2)}")
